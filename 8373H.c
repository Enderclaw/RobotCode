task usercontrol()
{
	while (true)
	{
		motor[FDL] = vexRT[Ch3];
		motor[FDL2] = vexRT[Ch3];

		motor[FDR] = vexRT[Ch2];
		motor[FDR2] = vexRT[Ch2];

		if(vexRT[Btn5U] == 1)
		{
			motor[LLT] = 127;
			motor[LLB] = 127;
			motor[RLT] = 127;
			motor[RLB] = 127;
		}

		else if(vexRT[Btn5D] == 1)
		{
			motor[LLT] = -127;
			motor[LLB] = -127;
			motor[RLT] = -127;
			motor[RLB] = -127;
		}

		else
		{
			motor[LLT] = 0;
			motor[LLB] = 0;
			motor[RLT] = 0;
			motor[RLB] = 0;
		}

		if(vexRT[Btn6U] == 1)
		{
			motor[Claw] = 127;
		}

		else if(vexRT[Btn6D] == 1)
		{
			motor[Claw] = -127;
		}

		else
		{
			motor[Claw] = 0;
		}
	}
}
